// Write a program to accept an integer value and print its table.
#include<stdio.h>
int main()
{
    int t;
    printf("Enter the integer : ");
    scanf("%d",&t);
    printf("Table of %d is as follows : \n",t);
    printf("%d \n",t*1);
    printf("%d \n",t*2);
    printf("%d \n",t*3);
    printf("%d \n",t*4);
    printf("%d \n",t*5);
    printf("%d \n",t*6);
    printf("%d \n",t*7);
    printf("%d \n",t*8);
    printf("%d \n",t*9);
    printf("%d \n",t*10);
    return 0;
}